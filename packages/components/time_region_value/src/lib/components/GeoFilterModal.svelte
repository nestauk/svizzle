<script>
	import Icon from '@svizzle/ui/src/icons/Icon.svelte';
	import Square from '@svizzle/ui/src/icons/feather/Square.svelte';
	import CheckSquare from '@svizzle/ui/src/icons/feather/CheckSquare.svelte';
	import {noop} from '@svizzle/utils';

	import {
		_nutsRegions,
		_preselectedNUTS1Id,
		deselectAllRegions,
		selectAllRegions,
		toggleRegionNUTS1,
	} from '../stores/regionSelection';

	const onMouseEnter = id => () => {
		$_preselectedNUTS1Id = id;
	}
	const onMouseLeave = () => () => {
		$_preselectedNUTS1Id = null;
	}
	const onClick = id => () => {
		toggleRegionNUTS1(id);
	}
</script>

<div
	class='clickable modal'
	on:click
>
	<div
		class='panel'
		on:click|stopPropagation={noop}
	>
		<h2>NUTS1 regions</h2>
		<ul>
			{#each $_nutsRegions as {id, name, selected}}
				<li
					class='clickable'
					on:click={onClick(id)}
					on:mouseenter={onMouseEnter(id)}
					on:mouseleave={onMouseLeave(id)}
				>
					<span class='checker'>
						{#if selected}
							<Icon
								glyph={CheckSquare}
								size={20}
							/>
						{:else}
							<Icon
								glyph={Square}
								size={20}
							/>
						{/if}
					</span>
					<span>{name}</span>
				</li>
			{/each}

			<li
				class='end sep clickable'
				on:click={() => selectAllRegions()}
			>
				<span>Select all</span>
				<span class='checker'>
					<Icon
						glyph={CheckSquare}
						size={20}
						stroke='palegreen'
					/>
				</span>
			</li>
			<li
				class='end clickable'
				on:click={() => deselectAllRegions()}
			>
				<span>Deselect all</span>
				<span class='checker'>
					<Icon
						glyph={Square}
						size={20}
						stroke='red'
					/>
				</span>
			</li>
		</ul>
	</div>
</div>

